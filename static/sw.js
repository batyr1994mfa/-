// Минимальный service worker — только для того, чтобы браузер разрешил
// установку сайта как приложения (значок в адресной строке / меню).
// Кеширования не делает — приложение всегда работает "живыми" запросами.
self.addEventListener("install", (event) => {
  self.skipWaiting();
});

self.addEventListener("activate", (event) => {
  self.clients.claim();
});

self.addEventListener("fetch", (event) => {
  // Намеренно НЕ вызываем event.respondWith() — все запросы обрабатывает
  // сам браузер как обычно, без вмешательства service worker'а. Раньше
  // здесь был "прозрачный" проброс fetch(event.request) через SW без
  // обработки ошибок: он не давал никакой пользы (ни кеша, ни офлайн-
  // режима), зато при провале ЛЮБОГО стороннего запроса — например,
  // несуществующей/битой ссылки на картинку в ответе ИИ — валился в
  // консоль необработанным "Uncaught TypeError: Failed to fetch". Простого
  // наличия обработчика fetch достаточно для PWA-инсталлируемости, реальный
  // перехват запросов не требуется.
});
